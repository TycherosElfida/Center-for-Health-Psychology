/** Returns all provinces sorted alphabetically by name. */
export function getProvinces(): Province[] {
  return [...PROVINCES].sort((a, b) => a.name.localeCompare(b.name));
}

/** Returns cities for a given province code, sorted alphabetically. */
export function getCitiesByProvince(provinceCode: string): City[] {
  return CITIES
    .filter((c) => c.provinceCode === provinceCode)
    .sort((a, b) => a.name.localeCompare(b.name));
}
